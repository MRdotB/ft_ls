#include "ft_ls.h"

char	*get_path(char *arg)
{
	char	*r;
	int		i;

	r = ft_strdup(arg);
	i = ft_strlen(r);
	if (r[i - 1] != '/')
		r = ft_concat(r, "/", 1);
	return (r);
}
